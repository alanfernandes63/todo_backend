package com.alanfernandes.todos.message;

public class MessageResponse {
	private String info;
	
	public MessageResponse() {
		super();
	}

	public MessageResponse(String info) {
		super();
		this.info = info;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}
	
	
}
